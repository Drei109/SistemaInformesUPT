package Entidad;

/**
 *
 * @author Drei
 */
public class ClsEntidadSemestre {

    private String idSemestre;
    private String nombreSemestre;
    private String anioSemestre;
    private String estadoSemestre;

    
    public String getIdSemestre() {
        return idSemestre;
    }

    public void setIdSemestre(String idSemestre) {
        this.idSemestre = idSemestre;
    }

    public String getNombreSemestre() {
        return nombreSemestre;
    }

    public void setNombreSemestre(String nombreSemestre) {
        this.nombreSemestre = nombreSemestre;
    }

    public String getAnioSemestre() {
        return anioSemestre;
    }

    public void setAnioSemestre(String anioSemestre) {
        this.anioSemestre = anioSemestre;
    }

    public String getEstadoSemestre() {
        return estadoSemestre;
    }

    public void setEstadoSemestre(String estadoSemestre) {
        this.estadoSemestre = estadoSemestre;
    }
}
