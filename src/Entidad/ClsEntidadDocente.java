package Entidad;

/**
 *
 * @author Drei
 */
public class ClsEntidadDocente {
    private String codDocente;
    private String nombreDocente;
    private String emailDocente;
    private String celularDocente;
    private String gradoDocente;

    public String getCodDocente() {
        return codDocente;
    }

    public void setCodDocente(String codDocente) {
        this.codDocente = codDocente;
    }

    public String getNombreDocente() {
        return nombreDocente;
    }

    public void setNombreDocente(String nombreDocente) {
        this.nombreDocente = nombreDocente;
    }

    public String getEmailDocente() {
        return emailDocente;
    }

    public void setEmailDocente(String emailDocente) {
        this.emailDocente = emailDocente;
    }

    public String getCelularDocente() {
        return celularDocente;
    }

    public void setCelularDocente(String celularDocente) {
        this.celularDocente = celularDocente;
    }

    public String getGradoDocente() {
        return gradoDocente;
    }

    public void setGradoDocente(String gradoDocente) {
        this.gradoDocente = gradoDocente;
    }
}
