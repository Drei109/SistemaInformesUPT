    private void cargarTabla() {
        String columnas[] = {
            "ID", "Plan Estudio", "Semestre", "Curso", "Matriculados",
            "Retirados", "Abandono", "Docente", "Seccion"};
        ArrayList<ClsNegocioCargaAcademica> dato = null;
        try {
            ClsNegocioCargaAcademica datos = new ClsNegocioCargaAcademica();
            dato = datos.MostrarCargaAcademica();
            Iterator iterator = dato.iterator();
            DefaultTableModel modeloTabla = new DefaultTableModel(null, columnas);
            modeloTabla.setRowCount(0);
            String campo[] = new String[9];
            
            while (iterator.hasNext()) {
                ClsEntidadCargaAcedemica objenti = new ClsEntidadCargaAcedemica();

                objenti = (ClsEntidadCargaAcedemica) iterator.next();

                campo[0] = String.valueOf(objenti.getIdCargaAcademica());
                campo[1] = objenti.getNombrePlanEstudio();
                campo[2] = objenti.getNombreSemestre();
                campo[3] = objenti.getIdCurso();
                campo[4] = String.valueOf(objenti.getAlumnosMatriculados());
                campo[5] = String.valueOf(objenti.getAlumnosRetirados());
                campo[6] = String.valueOf(objenti.getAlumnosAbandono());
                campo[7] = objenti.getNombreDocente();
                campo[8] = objenti.getSeccion();
                
                modeloTabla.addRow(campo);
            }
            
            tblCargaAcademica.setModel(modeloTabla);
            modeloTabla.fireTableDataChanged();
            datos.conexion.close();

        } catch (Exception e) {
            e.printStackTrace();
        }    
    }
    
    //CARGAR TODOS LOS PLANES DE ESTUDIOS EN EL COMBOBOX
    private void cargarComboPlanEstudio(){
        ArrayList<ClsNegocioPlanEstudio> datoPECB = null;
        
        try {
            ClsNegocioPlanEstudio datos = new ClsNegocioPlanEstudio();
            datoPECB = datos.MostrarPlanEstudios();
            Iterator iterator = datoPECB.iterator();
            
            DefaultComboBoxModel modelitoPE = new DefaultComboBoxModel();
            
            cmbPlanEstudio.removeAllItems();
            
            int con = 0;
            
            idPlanEstudioBD.clear();
            
            while (iterator.hasNext()) {
                ClsEntidadPlanEstudio objenti = new ClsEntidadPlanEstudio();

                objenti = (ClsEntidadPlanEstudio) iterator.next();

                idPlanEstudioBD.add(String.valueOf(objenti.getIdPlanEstudio()));
                
                modelitoPE.addElement(objenti.getNombrePlanEstudio());
                
                
            }
            
            cmbPlanEstudio.setModel(modelitoPE);
            datos.conexion.close();

        } catch (Exception e) {
            e.printStackTrace();
        } 
    }
    
    //CARGAR TODOS LOS SEMESTRES EN EL COMBOBOX
