    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        //IDS DE PLAN DE ESTUDIO
        String[] idsPE = new String[idPlanEstudioBD.size()];
        idsPE = idPlanEstudioBD.toArray(idsPE);
        
        //IDS DE SEMESTRE
        String[] idsSE = new String[idSemestreBD.size()];
        idsSE = idSemestreBD.toArray(idsSE);
        
        //IDS DE DOCENTES
        String[] idsDO = new String[idDocenteBD.size()];
        idsDO = idDocenteBD.toArray(idsDO);
        
        if (esNuevo) {
            activarTextos(true);
            activarBotones(true, false, false);
            limpiar();
            
            //
            txtMatriculados.setText("0");
            txtAbandono.setText("0");
            txtRetirados.setText("0");
            
            btnGuardar.setText("Guardar");
            esNuevo = false;
        } else {
            if (txtMatriculados.getText().equals("") || txtRetirados.getText().equals("") || txtAbandono.getText().equals("") ) {
                JOptionPane.showMessageDialog(null, "Complete el campo de Carga Academica antes de guardar");
            } else {
                ClsNegocioCargaAcademica negoCar = new ClsNegocioCargaAcademica();
                ClsEntidadCargaAcedemica entCar  = new ClsEntidadCargaAcedemica();
                
                entCar.setIdPlanEstudio(Integer.parseInt(idsPE[cmbPlanEstudio.getSelectedIndex()]));
                entCar.setIdSemestre(Integer.parseInt(idsSE[cmbSemestre.getSelectedIndex()]));
                entCar.setIdCurso(cmbCurso.getSelectedItem().toString());
                entCar.setAlumnosMatriculados(Integer.parseInt(txtMatriculados.getText()));
                entCar.setAlumnosRetirados(Integer.parseInt(txtRetirados.getText()));
                entCar.setAlumnosAbandono(Integer.parseInt(txtAbandono.getText()));
                entCar.setCodDocente(idsDO[cmbDocente.getSelectedIndex()]);
                entCar.setSeccion(cmbSeccion.getSelectedItem().toString());
                
                negoCar.AgregarCargaAcademica(entCar);

                limpiar();
                activarTextos(false);
                activarBotones(true, false, false);
                cargarTabla();
                btnGuardar.setText("Nuevo");
                esNuevo = true;
            }
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

