    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (esNuevo) {
            activarTextos(true);
            activarBotones(true, false, false);
            limpiar();
            cargarIdUsuario();
            btnGuardar.setText("Guardar");
            esNuevo = false;
        } else {           
            ClsNegocioUsuario negoUsu = new ClsNegocioUsuario();
            ClsEntidadUsuario entUsu = new ClsEntidadUsuario();
            entUsu.setCodDocente(txtCodDoc.getText());
            entUsu.setContraseniaUsuario(String.valueOf(txtContraseniaUsu.getPassword()));
            entUsu.setNivelUsuario((String) cmbNivelUsu.getSelectedItem());
            entUsu.setEstadoUsuario((String) cmbEstadoUsu.getSelectedItem());
            negoUsu.AgregarUsuario(entUsu);
            limpiar();
            activarTextos(false);
            activarBotones(true, false, false);
            cargarIdUsuario();
            cargarTablaDocentes();
            cargarTablaUsuario();
            btnGuardar.setText("Nuevo");
            esNuevo = true;
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

