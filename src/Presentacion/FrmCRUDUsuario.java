    private void cargarTablaUsuario() {
        String columnas[] = {"ID","Código","Nivel","Estado"};
        ArrayList<ClsNegocioUsuario> dato = null;
        try {
            ClsNegocioUsuario datos = new ClsNegocioUsuario();
            dato = datos.MostrarUsuarios();
            Iterator iterator = dato.iterator();
            DefaultTableModel modeloTabla = new DefaultTableModel(null, columnas);
            modeloTabla.setRowCount(0);
            String campo[] = new String[4];
            
            while (iterator.hasNext()) {
                ClsEntidadUsuario objenti = new ClsEntidadUsuario();

                objenti = (ClsEntidadUsuario) iterator.next();

                campo[0] = objenti.getIdUsuario();
                campo[1] = objenti.getCodDocente();
                campo[2] = objenti.getNivelUsuario();
                campo[3] = objenti.getEstadoUsuario();
                modeloTabla.addRow(campo);
            }
            
            tblUsuario.setModel(modeloTabla);
            modeloTabla.fireTableDataChanged();
            datos.conexion.close();

        } catch (Exception e) {
            e.printStackTrace();
        } 
    }

